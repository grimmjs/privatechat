  var RTL_LANGS = { ar: true, he: true, fa: true, ur: true };
  var STORAGE_KEY = "sc_lang";
  var listeners = [];
  var current = "en";

  function detectLang() {
    try {
      var saved = localStorage.getItem(STORAGE_KEY);
      if (saved && DICT[saved]) return saved;
    } catch (e) {}

    var langs = (navigator.languages && navigator.languages.length)
      ? navigator.languages
      : [navigator.language || navigator.userLanguage || "en"];

    for (var i = 0; i < langs.length; i++) {
      var l = String(langs[i] || "").toLowerCase();
      var primary = l.split("-")[0];
      if (DICT[primary]) return primary;
    }

    try {
      var tz = (Intl.DateTimeFormat().resolvedOptions().timeZone || "").toLowerCase();
      if (tz.indexOf("europe/rome") !== -1) return "it";
      if (tz.indexOf("europe/madrid") !== -1) return "es";
      if (tz.indexOf("europe/paris") !== -1) return "fr";
      if (tz.indexOf("europe/berlin") !== -1) return "de";
      if (tz.indexOf("europe/lisbon") !== -1 || tz.indexOf("america/sao_paulo") !== -1) return "pt";
      if (tz.indexOf("asia/tokyo") !== -1) return "ja";
      if (tz.indexOf("asia/shanghai") !== -1 || tz.indexOf("asia/hong_kong") !== -1) return "zh";
      if (tz.indexOf("europe/moscow") !== -1) return "ru";
    } catch (e) {}

    return "en";
  }

  function t(key) {
    var d = DICT[current] || EN;
    if (d[key]) return d[key];
    if (EN[key]) return EN[key];
    return key;
  }

  function apply(root) {
    root = root || document;
    document.documentElement.setAttribute("lang", current);
    document.documentElement.setAttribute("dir", RTL_LANGS[current] ? "rtl" : "ltr");

    var nodes = root.querySelectorAll("[data-i18n]");
    nodes.forEach(function (n) {
      var key = n.getAttribute("data-i18n");
      n.textContent = t(key);
    });
    var phNodes = root.querySelectorAll("[data-i18n-placeholder]");
    phNodes.forEach(function (n) {
      var key = n.getAttribute("data-i18n-placeholder");
      n.setAttribute("placeholder", t(key));
    });
    var titleNodes = root.querySelectorAll("[data-i18n-title]");
    titleNodes.forEach(function (n) {
      var key = n.getAttribute("data-i18n-title");
      n.setAttribute("title", t(key));
    });
    var ariaNodes = root.querySelectorAll("[data-i18n-aria]");
    ariaNodes.forEach(function (n) {
      var key = n.getAttribute("data-i18n-aria");
      n.setAttribute("aria-label", t(key));
    });
  }

  function setLang(lang) {
    if (!DICT[lang]) lang = "en";
    current = lang;
    try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
    apply(document);
    listeners.forEach(function (cb) { try { cb(lang); } catch (e) {} });
  }

  function getLang() { return current; }
  function listen(cb) { if (typeof cb === "function") listeners.push(cb); }
  function isRTL() { return !!RTL_LANGS[current]; }

  current = detectLang();

  window.I18n = {
    t: t, apply: apply, setLang: setLang, getLang: getLang,
    available: Object.keys(DICT), listen: listen, isRTL: isRTL,
  };

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", function () { apply(document); });
  } else {
    apply(document);
  }
